class Rectangle :
    def __init__ (self, width, length):
        self.width = width
        self.length = length 
    def perimetri (self) :
        p = 2*(self.width + self.length)
        return p
    def fartobi(self) :
        s = self.width * self.length
        return s

obj = Rectangle(1,5)

print(obj.perimetri())
print(obj.fartobi())



