class Bank_Account:
    def __init__(self,  account_name, balance = 0):
        self.account_name = account_name
        self.balance = balance
    def get_balance(self):
        return self.balance
    def set_balance(self, lari):
        self.balance = lari
        return self.balance
    def deposit(self, money):
        self.money = money
        m = self.money + self.balance
        return f"Tqven charicxet fuli da tqveni angarishi udris: {m} lars"
    def withdraw(self, cash):
        self.cash = cash
        c = self.balance - self.cash
        return f"Tqven gaqvt agebuli krediti, amitomac tqveni balanci sheadgens: {c} lars"
        
        
